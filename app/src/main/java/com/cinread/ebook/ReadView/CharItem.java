public class CharItem {
  private int x;
  private int y;
  private char value;

  public char getValue() {
    return value;
  }

  public void setValue(char value) {
    this.value = value;
  }

  public int getX() {
    return x;
  }

  public void setX(int x) {
    this.x = x;
  }

  public int getY() {
    return y;
  }

  public void setY(int y) {
    this.y = y;
  }
}
